export function getButterflyEcho(game, caseId) {
  const d = game.decisions || {}
  const s = game.refreshments || {}
  if (caseId === 'key' && d.bird === 'return') return '男孩的雨衣肩头停着一根黄铜羽毛。他说白榆街刚长出的楼层挡住了回家的路。'
  if (caseId === 'key' && d.bird === 'auction') return '男孩递来的钥匙齿缝里夹着一根湿羽毛；它来自一位没有留下姓名的买家。'
  if (caseId === 'jar' && d.key === 'return') return '她脚边的影子穿着男孩的雨靴。影子看见你后，先一步躲进糖罐。'
  if (caseId === 'jar' && d.key === 'seal') return '她的倒影总比本人慢三秒，恰好和你的影子一样。'
  if (caseId === 'obituary' && d.bird === 'return') return '退信地址写着“白榆街 8 号，楼上”。玛戈坚持那栋房子昨天还没有楼上。'
  if (caseId === 'obituary' && d.jar === 'return') return '玛戈开口时，先传来的是没有倒影太太的声音：“别让她读收件人。”'
  if (caseId === 'mask' && d.obituary === 'return') return '节目单背面印着全镇共同梦见的葬礼。主角栏里仍是你的名字。'
  if (caseId === 'mask' && d.key === 'return') return '第十三场的道具单多出一件黄色雨衣。塞拉菲娜说那位小演员终于回家了。'
  if (caseId === 'watch' && d.mask === 'auction') return '伊莱亚斯身后的空椅坐满了戴着你脸的买家。他们没有影子，也没有出价牌。'
  if (caseId === 'watch' && s.obituary === 'rain') return '怀表盖上的湿指纹与你在“门前雨”旁留下的完全一致——包括一道你手上没有的旧伤。'
  return ''
}

